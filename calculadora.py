primeiraEntrada = int(input('Digite o primeiro valor: '))
segundaEntrada = int(input('Digite o segundo valor: '))


soma = primeiraEntrada + segundaEntrada
subtracao = primeiraEntrada - segundaEntrada
multiplicacao = primeiraEntrada * segundaEntrada
divisao = primeiraEntrada // segundaEntrada

print(f"Resultado da adição: {soma}")
print(f"Resultado da subtração: {subtracao}")
print(f"Resultado da multiplicação: {multiplicacao}")
print(f"Resultado da divisão inteira: {divisao}")
